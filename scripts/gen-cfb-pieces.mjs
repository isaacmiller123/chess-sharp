// Generates src/renderer/src/games/boards/chess-family-pieces.css — the app's
// existing chess piece sets re-targeted at chessgroundx role classes.
//
//   node scripts/gen-cfb-pieces.mjs
//
// chessground (the classic app board) names piece roles `piece.queen.white`;
// chessgroundx (games platform board, ChessFamilyBoard.tsx) names them by FEN
// letter: `piece.q-piece.white`. The artwork itself is identical, so instead of
// re-embedding SVGs we parse the data URIs out of the already-bundled sources
// and emit the same images under chessgroundx selectors, scoped to `.cfb-wrap`:
//   - default set (cburnett): node_modules/chessground/assets/chessground.cburnett.css
//   - merida/chessnut/fantasy/pirouetti: src/renderer/src/styles/pieces.css
// Also emits the makruk stand-ins (met→queen art, khon→bishop art) —
// TODO(P2 polish): dedicated makruk piece art (e.g. PyChess 'ada' set, CC0).
//
// Output is committed; re-run only when piece sets change.

import { readFileSync, writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, resolve } from 'node:path'

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..')

const ROLE_LETTER = {
  pawn: 'p',
  knight: 'n',
  bishop: 'b',
  rook: 'r',
  queen: 'q',
  king: 'k'
}

/** selector-fragment → data-uri, e.g. 'merida|queen|white' → 'data:image/...' */
function parseCss(cssText, defaultSet) {
  const out = new Map()
  // One rule per block: selectors { decls }
  for (const block of cssText.split('}')) {
    const i = block.indexOf('{')
    if (i < 0) continue
    const selectors = block.slice(0, i)
    const decls = block.slice(i + 1)
    const url = decls.match(/background-image:\s*url\((['"]?)(data:[^'")]+)\1\)/)
    if (!url) continue
    for (const sel of selectors.split(',')) {
      const m = sel.match(/(?:\.pieces-([a-z0-9]+)\s+)?\.cg-wrap\s+piece\.(\w+)\.(white|black)/)
      if (!m) continue
      const set = m[1] ?? defaultSet
      const role = m[2]
      if (!(role in ROLE_LETTER)) continue
      out.set(`${set}|${role}|${m[3]}`, url[2])
    }
  }
  return out
}

const images = new Map([
  ...parseCss(
    readFileSync(resolve(ROOT, 'node_modules/chessground/assets/chessground.cburnett.css'), 'utf8'),
    'cburnett'
  ),
  ...parseCss(readFileSync(resolve(ROOT, 'src/renderer/src/styles/pieces.css'), 'utf8'), null)
])

const sets = [...new Set([...images.keys()].map((k) => k.split('|')[0]))].filter((s) => s !== 'null')
const lines = [
  '/* GENERATED by scripts/gen-cfb-pieces.mjs — do not edit by hand.',
  '   Chess piece sets for chessgroundx boards (.cfb-wrap): the exact artwork of',
  '   the app piece sets (chessground.cburnett.css + styles/pieces.css), keyed by',
  "   chessgroundx role classes (piece.q-piece.white). 'cburnett' rules are",
  '   unprefixed (the app default); other sets override under .pieces-<set>.',
  '   Makruk met/khon reuse queen/bishop art — TODO(P2 polish): dedicated set. */',
  ''
]

let count = 0
for (const set of sets) {
  const prefix = set === 'cburnett' ? '' : `.pieces-${set} `
  for (const [role, letter] of Object.entries(ROLE_LETTER)) {
    for (const color of ['white', 'black']) {
      const uri = images.get(`${set}|${role}|${color}`)
      if (!uri) {
        console.error(`MISSING art: ${set} ${role} ${color}`)
        process.exitCode = 1
        continue
      }
      lines.push(
        `${prefix}.cfb-wrap piece.${letter}-piece.${color} { background-image: url('${uri}'); }`
      )
      count++
    }
  }
  // Makruk stand-ins: met (m) ≈ queen, khon (s) ≈ bishop.
  for (const [letter, role] of [
    ['m', 'queen'],
    ['s', 'bishop']
  ]) {
    for (const color of ['white', 'black']) {
      const uri = images.get(`${set}|${role}|${color}`)
      if (!uri) continue
      lines.push(
        `${prefix}.cfb-wrap.cfb-makruk piece.${letter}-piece.${color} { background-image: url('${uri}'); }`
      )
      count++
    }
  }
}

const outPath = resolve(ROOT, 'src/renderer/src/games/boards/chess-family-pieces.css')
writeFileSync(outPath, lines.join('\n') + '\n')
console.log(`wrote ${outPath}: ${count} rules across sets: ${sets.join(', ')}`)
